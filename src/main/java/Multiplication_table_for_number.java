public class Multiplication_table_for_number {
    /**
     * https://www.codewars.com/kata/5a2fd38b55519ed98f0000ce/train/java
     */

    public static String multiTable(int num) {
        String str = "1 * " + num + " = " + 1 * num + "\n" +
                "2 * " + num + " = " + 2 * num + "\n" +
                "3 * " + num + " = " + 3 * num + "\n" +
                "4 * " + num + " = " + 4 * num + "\n" +
                "5 * " + num + " = " + 5 * num + "\n" +
                "6 * " + num + " = " + 6 * num + "\n" +
                "7 * " + num + " = " + 7 * num + "\n" +
                "8 * " + num + " = " + 8 * num + "\n" +
                "9 * " + num + " = " + 9 * num + "\n" +
                "10 * " + num + " = " + 10 * num;

        return str;
    }
}
